public class FunctionBlock extends CodeBlock{

    /**
     * @author alexbanham
     * Creates a FunctionBlock object
     * @param wrapper the drawable component
     */
    public FunctionBlock(Drawable wrapper) {
        super(wrapper,1,1);
    }

    public void setXCenter(int x) {
        setXCenter(x);
    }

    public void setYCenter(int y) {
        setYCenter(y);
    }
}
